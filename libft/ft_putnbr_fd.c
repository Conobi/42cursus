#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	r;

	if (n == 0)
		r = '0';
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			if (n < (-9))
				ft_putnbr_fd(n / 10 * (-1), fd);
			r = n % 10 * (-1) + '0';
		}
		else
		{
			if (n > 9)
				ft_putnbr_fd(n / 10, fd);
			r = n % 10 + '0';
		}
	}
	write(fd, &r, 1);
}
