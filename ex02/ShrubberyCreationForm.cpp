static bool write_file(const std::string filename, const std::string content) {
	std::ofstream out_file(filename);

	if (out_file.is_open()) {
		out_file << content;
		out_file.close();
		return (true);
	}
	std::cerr << RED_FG << "An error occured while writing to `" << filename
			  << "`." << RESET << std::endl;
	return (false);
}

/* OVERRIDING EXECUTOR BASE METHOD */

void ShrubberyCreationForm::_executor() const {
	write_file(this->getTarget() + "_shrubbery", ascii_trees());
}
